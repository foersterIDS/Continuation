                  'l_start',l_start,...
                  'l_end',l_end);
	%
    %% Path
    %
    Path = struct('var_all',[],...
                  'l_all',[],...
                  's_all',[]);
	%
    %% Plot
    %
    Plot = struct('fig',[],...
